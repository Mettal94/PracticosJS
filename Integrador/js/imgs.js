function imgIzquierda() {
    for (var i = 0; i < pos.length; i++) {
        if (pos[i] == 0) {
            pos[i] = arreglo.length - 1;
        } else {
            pos[i] = pos[i] - 1;
        }
    }
    imgizq.src = arreglo[pos[0]].img;
    imgcen.src = arreglo[pos[1]].img;
    imgder.src = arreglo[pos[2]].img;
   cargarDescripcion()
}

